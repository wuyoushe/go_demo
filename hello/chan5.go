package main

import "fmt"

func calcSquares(number int, squareop chan int){
	sum := 0
	for number != 0 {
		digit := number % 10
		sum += digit * digit
		number /= 10
	}
	squareop <- sum
}

func calCubes(number int, cubeop chan int) {
	sum := 0
	for number != 0 {
		digit := number % 10
		sum += digit * digit * digit
		number /= 10
	}
	cubeop <- sum
}

func main() {
	number := 589
	sqrch := make(chan int)
	cubech := make(chan int)
	go calcSquares(number, sqrch)
	go calCubes(number, cubech)
	squares, cubes := <- sqrch, <-cubech
	fmt.Print("Final output", squares + cubes)
}
